// @flow

import * as React from 'react';
import { Trans } from '@lingui/macro';
import Dialog, { DialogPrimaryButton } from '../../../../../UI/Dialog';
import FlatButton from '../../../../../UI/FlatButton';
import LeftLoader from '../../../../../UI/LeftLoader';
import Form from '../../../../../UI/Form';
import { ColumnStackLayout } from '../../../../../UI/Layout';
import TextField from '../../../../../UI/TextField';
import { emailRegex } from '../../../../../Utils/EmailUtils';
import AlertMessage from '../../../../../UI/AlertMessage';
import { extractGDevelopApiErrorStatusAndCode } from '../../../../../Utils/GDevelopServices/Errors';

type InviteStudentError =
  | 'no-seats-available'
  | 'user-not-found'
  | 'user-already-added'
  | 'user-has-subscription'
  | 'user-with-autogenerated-email'
  | 'user-owner'
  | 'not-allowed'
  | 'unexpected';

const inviteErrorToText = {
  'no-seats-available': (
    <Trans>Your team does not have enough seats for a new student.</Trans>
  ),
  'user-not-found': <Trans>The email you provided could not be found.</Trans>,
  'user-already-added': (
    <Trans>The email you provided is already a member of your team.</Trans>
  ),
  'user-has-subscription': (
    <Trans>
      The email you provided already has a subscription with GDevelop. Please
      ask them to cancel it before adding them as a student in your team.
    </Trans>
  ),
  'user-with-autogenerated-email': (
    <Trans>This account is already a student in another team.</Trans>
  ),
  'user-owner': (
    <Trans>The main account of the Education plan cannot be modified.</Trans>
  ),
  'not-allowed': (
    <Trans>You don't have enough rights to invite a new student.</Trans>
  ),
  unexpected: (
    <Trans>
      An unexpected error happened. Please contact us for more details.
    </Trans>
  ),
};

type Props = {|
  onClose: () => void,
  onInviteStudent: (email: string) => Promise<void>,
|};

const InviteStudentDialog = ({
  onClose,
  onInviteStudent,
}: Props): React.Node => {
  const [email, setEmail] = React.useState<string>('');
  const [actionInProgress, setActionInProgress] = React.useState(false);
  const [isEmailValid, setIsEmailValid] = React.useState<boolean>(true);
  const [inviteError, setInviteError] = React.useState<?InviteStudentError>(
    null
  );
  const [lastInvitedEmail, setLastInvitedEmail] = React.useState<?string>(null);

  const doInviteStudent = React.useCallback(
    async () => {
      if (!email || !emailRegex.test(email)) {
        setIsEmailValid(false);
        return;
      }
      setActionInProgress(true);
      setInviteError(null);
      setLastInvitedEmail(null);
      try {
        await onInviteStudent(email);
        setLastInvitedEmail(email);
        setEmail('');
        setIsEmailValid(true);
      } catch (error) {
        console.error(error);
        const extractedStatusAndCode = extractGDevelopApiErrorStatusAndCode(
          error
        );
        if (!extractedStatusAndCode) {
          setInviteError('unexpected');
        } else {
          let inviteStudentError;
          if (extractedStatusAndCode.status === 404) {
            inviteStudentError = 'user-not-found';
          } else if (extractedStatusAndCode.status === 403) {
            inviteStudentError = 'not-allowed';
          } else if (
            extractedStatusAndCode.code ===
            'team-member-creation/user-with-autogenerated-email'
          ) {
            inviteStudentError = 'user-with-autogenerated-email';
          } else if (
            extractedStatusAndCode.code ===
            'team-member-creation/owner-cannot-be-modified'
          ) {
            inviteStudentError = 'user-owner';
          } else if (
            extractedStatusAndCode.code === 'team-member-creation/team-full'
          ) {
            inviteStudentError = 'no-seats-available';
          } else if (
            extractedStatusAndCode.code ===
            'team-member-creation/user-already-member'
          ) {
            inviteStudentError = 'user-already-added';
          } else if (
            extractedStatusAndCode.code ===
            'team-member-creation/user-has-subscription'
          ) {
            inviteStudentError = 'user-has-subscription';
          } else {
            inviteStudentError = 'unexpected';
          }
          // $FlowFixMe[incompatible-type]
          setInviteError(inviteStudentError);
        }
      } finally {
        setActionInProgress(false);
      }
    },
    [email, onInviteStudent]
  );

  return (
    <Dialog
      title={<Trans>Invite a student</Trans>}
      actions={[
        <FlatButton
          label={<Trans>Back</Trans>}
          disabled={actionInProgress}
          key="back"
          primary={false}
          onClick={onClose}
        />,
        <LeftLoader isLoading={actionInProgress} key="invite-student">
          <DialogPrimaryButton
            label={<Trans>Invite</Trans>}
            primary
            onClick={doInviteStudent}
            disabled={actionInProgress}
          />
        </LeftLoader>,
      ]}
      maxWidth="sm"
      cannotBeDismissed={actionInProgress}
      onRequestClose={onClose}
      onApply={doInviteStudent}
      open
    >
      <Form onSubmit={doInviteStudent} name="inviteStudent">
        <ColumnStackLayout noMargin>
          <AlertMessage kind="info">
            <Trans>
              You can add students who already have a GDevelop account. They
              will receive a notification to accept the invitation. If they
              don't have an account, they can create one with their student
              email.
            </Trans>
          </AlertMessage>
          <TextField
            autoFocus="desktop"
            value={email}
            floatingLabelText={<Trans>Email</Trans>}
            type="email"
            onChange={(e, value) => {
              if (!isEmailValid) setIsEmailValid(true);
              if (inviteError) setInviteError(null);
              setEmail(value);
            }}
            errorText={
              !isEmailValid ? <Trans>Invalid email address.</Trans> : null
            }
            fullWidth
            onBlur={event => {
              const trimmedEmail = event.currentTarget.value.trim();
              setEmail(trimmedEmail);
              if (trimmedEmail) {
                setIsEmailValid(emailRegex.test(trimmedEmail));
              }
            }}
            disabled={actionInProgress}
          />
          {lastInvitedEmail && (
            <AlertMessage kind="valid">
              <Trans>Invitation sent to {lastInvitedEmail}.</Trans>
            </AlertMessage>
          )}
          {inviteError && (
            <AlertMessage kind="error">
              {inviteErrorToText[inviteError]}
            </AlertMessage>
          )}
        </ColumnStackLayout>
      </Form>
    </Dialog>
  );
};

export default InviteStudentDialog;
